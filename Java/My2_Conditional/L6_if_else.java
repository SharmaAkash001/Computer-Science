package Telsuko.My2_Conditional;

public class L6_if_else
{
    public static void main(String[] args)
    {
        int a = 15;

        if (a > 10 && a <= 20)
        {
            System.out.println("Hello There");
        }

        int x = 4;
        int y = 7;

        if (x > y)
        {
            System.out.println(x);
        }
        else
        {
            System.out.println(y);
        }
    }
}
